function evaluatePasswordStrength(password) {
  // Проверяем длину пароля
  if (password.length < 8) {
    return "Слишком короткий пароль. Рекомендуется использовать не менее 8 символов.";
  }

  // Проверяем наличие букв в разных регистрах
  if (!/[a-z]/.test(password) || !/[A-Z]/.test(password)) {
    return "Пароль должен содержать как минимум одну букву в верхнем и одну букву в нижнем регистре.";
  }

  // Проверяем наличие цифр
  if (!/\d/.test(password)) {
    return "Пароль должен содержать как минимум одну цифру.";
  }

  // Проверяем наличие специальных символов
  if (!/[!@#$%^&*(),.?":{}|<>]/.test(password)) {
    return 'Пароль должен содержать как минимум один специальный символ, например: !@#$%^&*(),.?":{}|<>.';
  }

  // Если все условия выполнены, пароль считается сильным
  return "Пароль сильный. Хорошая работа!";
}

// Пример использования функции
var password = prompt("Введите пароль:");
var strength = evaluatePasswordStrength(password);
console.log(strength);
